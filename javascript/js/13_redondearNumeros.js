/* redondear hacia arriba con metodo ceil */

var numero4 = 7.1
var numero4Redondeado = Math.ceil (numero4);
console.log(numero4Redondeado);

/* redondear hacia abajo  con metodo floor*/ 

var numero5 = 7.9
var numero5Redondeado = Math.floor (numero5);
console.log(numero5Redondeado);

